import { z } from "zod";

export const instituteValidation = z.object({
    name: z.string().min(2),
    slug: z.string().min(2),
    phone: z.string().optional(),
    websiteUrl: z.string().url().optional(),
    instagramUrl: z.string().url().optional(),
    facebookUrl: z.string().url().optional(),
    youtubeUrl: z.string().url().optional(),
});
